"""OPP — Руководство пользователя (.docx). Сборка асcета продукта из reference/guide/руководство.md.

Исходник содержания — руководство.md (человекочитаемый, правится текстом). Этот скрипт держит
только вёрстку (стили, таблицы, врезки) и **сам текст глав** — синхронизирован с руководство.md
вручную (полноценный md→docx-парсер здесь избыточен: гайд небольшой и меняется редко). Правите
содержание — правьте оба файла.

Запуск: python3 reference/guide/build.py → reference/guide/«OPP — Руководство пользователя.docx»
"""
from docx import Document
from docx.shared import Pt, RGBColor, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.enum.table import WD_TABLE_ALIGNMENT
from docx.oxml.ns import qn
from docx.oxml import OxmlElement
from pathlib import Path

ACCENT = RGBColor(0x1F, 0x38, 0x64)      # тёмно-синий — заголовки
ACCENT_BG = "1F3864"                      # заливка шапок таблиц
LIGHT = "EEF1F7"                          # светлая заливка врезок/зебры
GREY = RGBColor(0x55, 0x55, 0x55)
WARN = RGBColor(0x99, 0x30, 0x00)         # «в разработке»
FONT = "Calibri"

doc = Document()

# --- базовый стиль ---
normal = doc.styles["Normal"]
normal.font.name = FONT
normal.font.size = Pt(10.5)
normal.paragraph_format.space_after = Pt(6)
normal.paragraph_format.line_spacing = 1.12


def _set_cell_bg(cell, hex_color):
    tcPr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:fill"), hex_color)
    tcPr.append(shd)


def _no_space(p):
    p.paragraph_format.space_before = Pt(0)
    p.paragraph_format.space_after = Pt(0)


def h(level, text):
    p = doc.add_heading(text, level=level)
    for run in p.runs:
        run.font.color.rgb = ACCENT
        run.font.name = FONT
    return p


def para(text=None, italic=False, bold=False, color=None, size=None, after=6, align=None):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(after)
    if align is not None:
        p.alignment = align
    if text:
        r = p.add_run(text)
        r.italic = italic
        r.bold = bold
        r.font.name = FONT
        if color is not None:
            r.font.color.rgb = color
        if size is not None:
            r.font.size = Pt(size)
    return p


def rich(parts, after=6):
    """parts: list of (text, {bold,italic,color})."""
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(after)
    for text, fmt in parts:
        r = p.add_run(text)
        r.font.name = FONT
        r.bold = fmt.get("bold", False)
        r.italic = fmt.get("italic", False)
        if fmt.get("color"):
            r.font.color.rgb = fmt["color"]
    return p


def bullet(text, bold_prefix=None):
    p = doc.add_paragraph(style="List Bullet")
    p.paragraph_format.space_after = Pt(3)
    if bold_prefix:
        r = p.add_run(bold_prefix)
        r.bold = True
        r.font.name = FONT
        r2 = p.add_run(text)
        r2.font.name = FONT
    else:
        r = p.add_run(text)
        r.font.name = FONT
    return p


def callout(title, text):
    """Врезка: одноячеечная таблица со светлой заливкой и акцентной левой границей."""
    t = doc.add_table(rows=1, cols=1)
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    cell = t.cell(0, 0)
    cell.width = Cm(16.5)
    _set_cell_bg(cell, LIGHT)
    tcPr = cell._tc.get_or_add_tcPr()
    borders = OxmlElement("w:tcBorders")
    left = OxmlElement("w:left")
    left.set(qn("w:val"), "single")
    left.set(qn("w:sz"), "18")
    left.set(qn("w:space"), "0")
    left.set(qn("w:color"), ACCENT_BG)
    borders.append(left)
    tcPr.append(borders)
    p0 = cell.paragraphs[0]
    _no_space(p0)
    r = p0.add_run(title)
    r.bold = True
    r.font.name = FONT
    r.font.color.rgb = ACCENT
    r.font.size = Pt(10)
    p1 = cell.add_paragraph()
    _no_space(p1)
    r1 = p1.add_run(text)
    r1.font.name = FONT
    r1.font.size = Pt(10)
    doc.add_paragraph().paragraph_format.space_after = Pt(2)


def table(headers, rows, widths_cm):
    t = doc.add_table(rows=1, cols=len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.style = "Table Grid"
    hdr = t.rows[0].cells
    for i, htext in enumerate(headers):
        _set_cell_bg(hdr[i], ACCENT_BG)
        p = hdr[i].paragraphs[0]
        _no_space(p)
        r = p.add_run(htext)
        r.bold = True
        r.font.name = FONT
        r.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
        r.font.size = Pt(9.5)
    for ri, row in enumerate(rows):
        cells = t.add_row().cells
        for ci, val in enumerate(row):
            if ri % 2 == 1:
                _set_cell_bg(cells[ci], LIGHT)
            p = cells[ci].paragraphs[0]
            _no_space(p)
            segs = val if isinstance(val, list) else [(val, {})]
            for seg_text, seg_fmt in segs:
                r = p.add_run(seg_text)
                r.font.name = FONT
                r.font.size = Pt(9)
                r.bold = seg_fmt.get("bold", ci == 0)
                if seg_fmt.get("italic"):
                    r.italic = True
                if seg_fmt.get("color"):
                    r.font.color.rgb = seg_fmt["color"]
    for row in t.rows:
        for ci, w in enumerate(widths_cm):
            row.cells[ci].width = Cm(w)
    doc.add_paragraph().paragraph_format.space_after = Pt(2)
    return t


def add_toc():
    p = doc.add_paragraph()
    run = p.add_run()
    fld1 = OxmlElement("w:fldChar")
    fld1.set(qn("w:fldCharType"), "begin")
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = 'TOC \\o "1-2" \\h \\z \\u'
    fld2 = OxmlElement("w:fldChar")
    fld2.set(qn("w:fldCharType"), "separate")
    t = OxmlElement("w:t")
    t.text = "Оглавление обновится в Word: правый клик → Обновить поле."
    fld3 = OxmlElement("w:fldChar")
    fld3.set(qn("w:fldCharType"), "end")
    run._r.append(fld1)
    run._r.append(instr)
    run._r.append(fld2)
    run._r.append(t)
    run._r.append(fld3)


