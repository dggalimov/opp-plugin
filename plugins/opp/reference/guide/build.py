pPr = lp._p.get_or_add_pPr()
pbdr = OxmlElement("w:pBdr")
bottom = OxmlElement("w:bottom")
bottom.set(qn("w:val"), "single")
bottom.set(qn("w:sz"), "12")
bottom.set(qn("w:space"), "1")
bottom.set(qn("w:color"), ACCENT_BG)
pbdr.append(bottom)
pPr.append(pbdr)
para("Черновик для внутреннего использования. Продукт в развитии — часть шагов помечена «в разработке».",
     italic=True, size=9.5, color=GREY, after=6)

doc.add_page_break()
h(1, "Оглавление")
add_toc()
doc.add_page_break()

