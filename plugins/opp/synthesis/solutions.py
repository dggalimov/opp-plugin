"""cap-t-solutions — движок «решения» T-конвейера осмысления (Ф5: осмысление, спека 06-d).

Отчёт по решениям (SOL) и их покрытию требований (TB): каждое согласованное требование обязано
иметь хотя бы одно живое решение (SOL.закрываемое требование → TB, замкнутость р.7); решение без
живой ссылки на TB — дефект «решение без требования» (SOL в воздухе). Тип решения (SOL.тип
решения) должен быть согласован с типом корневой проблемы (PRB.координата 4 — тип решения через
SOL.устраняемая проблема → PRB) — рассогласование в отчёт как дефект (методика C6, тот же
принцип, что `linter.checks._check_type_vs_level`, но здесь сверяем SOL↔PRB, а не PRB↔уровень).

Это РЕЛЬСЫ, не суждение: подбор решений и продуктов делает навык `skill-solutions` — здесь только
проверка полноты покрытия и согласованности уже написанных строк.

Переиспользует: `linter.model.load_memory` (строки проекта).
"""

from __future__ import annotations
from pathlib import Path

from linter.model import load_memory

# TB.статус требования, с которых требование ожидает решение-ответ (словарь TB, не PRB —
# у требования своя ось: заявлено → подтверждено → согласовано → отклонено).
_ТРЕБУЕТ_РЕШЕНИЯ = {"подтверждено", "согласовано"}


def _rows(memory, table) -> list:
    return [r for r in memory.rows if r.table == table]


def _index_by(rows: list, key: str) -> dict:
    return {r.data.get(key): r for r in rows if r.data.get(key) not in (None, "")}


def _type_prefix(тип: str) -> str:
    """Тип решения PRB несёт суффикс диапазона уровней («методологическое (Ур.1–3)»);
    тип решения SOL — голое слово из словаря. Сверяем по префиксу (как C6 в linter.checks)."""
    return (тип or "").split(" (")[0].strip()


def solutions_report(workspace) -> dict:
    """Отчёт по решениям (SOL) и покрытию требований.

    Замкнутость TB→SOL: каждое согласованное требование (TB) имеет хотя бы одну живую строку
    SOL с «закрываемое требование» на него — иначе «требование без решения». Обратная сторона:
    SOL с несуществующей/пустой ссылкой на TB (или на PRB) — дефект «решение без требования».
    Тип решения SOL должен совпадать (по префиксу до диапазона уровней) с типом корневой
    проблемы, устраняемой этим решением (SOL.устраняемая проблема → PRB.координата 4) —
    рассогласование в дефекты. Счётчики покрытия — по числу согласованных TB, закрытых SOL.
    """
    memory = load_memory(Path(workspace))
    tb_index = _index_by(_rows(memory, "TB"), "код требования")
    prb_index = _index_by(_rows(memory, "PRB"), "код проблемы")
    sol_rows = _rows(memory, "SOL")

    дефекты: list = []
    покрыто_tb = set()

    for row in sol_rows:
        код = row.data.get("код решения")
        требование = row.data.get("закрываемое требование")
        проблема = row.data.get("устраняемая проблема")
        недостачи = []

        if not (требование and требование in tb_index):
            недостачи.append("ссылка «закрываемое требование» не резолвится в TB")
        else:
            покрыто_tb.add(требование)

        if not (проблема and проблема in prb_index):
            недостачи.append("ссылка «устраняемая проблема» не резолвится в PRB")

        if недостачи:
            дефекты.append({"код решения": код, "дефект": "решение без требования",
                            "детали": недостачи})
            continue  # без обеих ссылок тип решения сверять не с чем

        тип_решения = _type_prefix(row.data.get("тип решения"))
        prb_row = prb_index[проблема]
        тип_проблемы = _type_prefix(prb_row.data.get("координата 4 — тип решения"))
        if тип_решения and тип_проблемы and тип_решения != тип_проблемы:
            дефекты.append({
                "код решения": код, "дефект": "тип решения не согласован с типом проблемы",
                "тип решения (SOL)": тип_решения, "тип проблемы (PRB, координата 4)": тип_проблемы,
                "устраняемая проблема": проблема,
            })

    непокрытые_требования = []
    for row in _rows(memory, "TB"):
        код = row.data.get("код требования")
        статус = row.data.get("статус требования")
        if статус in _ТРЕБУЕТ_РЕШЕНИЯ and код not in покрыто_tb:
            непокрытые_требования.append({"код требования": код, "статус требования": статус})

    return {
        "этап": "осмысление — решения (SOL) и покрытие требований",
        "счётчики": {
            "TB в памяти": len(tb_index),
            "SOL в памяти": len(sol_rows),
            "TB покрыто решением": len(покрыто_tb & set(tb_index)),
            "дефектов": len(дефекты),
            "непокрытых требований": len(непокрытые_требования),
        },
        "дефекты": дефекты,
        "непокрытые_требования": непокрытые_требования,
        "правило": "каждое требование покрыто решением (SOL→TB, р.7); решение без требования — "
                   "«решение в воздухе»; тип решения согласован с типом корневой проблемы (C6)",
    }
