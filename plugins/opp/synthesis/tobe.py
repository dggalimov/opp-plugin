"""cap-t-tobe — движок «целевая картина» T-конвейера осмысления (Ф5: осмысление, спека 06-d).

TLM («Целевая модель по уровням») — таблица-ПРЕДСТАВЛЕНИЕ (`тип: представление` в контракте):
ручные строки в ней запрещены (`linter.checks._check_view_readonly`, C3) — все поля выводимые,
её некому и незачем писать в память проекта. Поэтому этот отчёт не читает `memory/TLM.yaml`
(там пусто по конструкции), а вычисляет ту же проекцию «на лету» из уже написанных решений (SOL,
поле «уровень устранения» — тот же словарь, что уровни каркаса) — симметрично тому, как
`synthesis.triangulate` строит проекцию AS-IS по уровням из первичных таблиц, не читая
представление `asis-levels`.

Отчёт: (1) каждое решение (элемент будущей TLM) обосновано — несёт живую ссылку на TB и PRB
(без этого «TO-BE из воздуха», тот же принцип, что дефект `linter.checks._check_gate_obe`, но
здесь уже проверено `synthesis.solutions` — тут не дублируем, только считаем непокрытые уровни);
(2) симметрия каркасу (11 уровней, `reference/framework.yaml`): для каждого уровня — есть ли
целевой образ (хотя бы одно SOL с этим «уровень устранения») либо явная пометка уровня «без
изменений» (SOL с формулировкой/примечанием, фиксирующим отсутствие изменений — методика р.9);
(3) «белые пятна TO-BE» — уровни AS-IS, где есть доказанная проблема (PRB статус выше
«гипотезы») без единого целевого образа решения на этом уровне.

Переиспользует: `linter.model.load_memory` (строки проекта), `linter.checks._ВЫШЕ_ГИПОТЕЗЫ`
(порог доказанности PRB, с которого уровень AS-IS ожидает целевой образ).
"""

from __future__ import annotations
from collections import defaultdict
from pathlib import Path

import yaml

from linter.model import load_memory
from linter.checks import _ВЫШЕ_ГИПОТЕЗЫ

ROOT = Path(__file__).resolve().parent.parent
_БЕЗ_ИЗМЕНЕНИЙ = "без изменений"  # маркер в «примечание»/«обоснование выбора» SOL — явная пометка уровня


def _rows(memory, table) -> list:
    return [r for r in memory.rows if r.table == table]


def _index_by(rows: list, key: str) -> dict:
    return {r.data.get(key): r for r in rows if r.data.get(key) not in (None, "")}


def _framework_levels() -> list:
    """Номера и названия 11 уровней каркаса из reference/framework.yaml (источник истины)."""
    data = yaml.safe_load((ROOT / "reference" / "framework.yaml").read_text(encoding="utf-8")) or {}
    return [{"номер": lv.get("номер"), "название": lv.get("название")}
            for lv in data.get("уровни", [])]


def tobe_report(workspace) -> dict:
    """Отчёт по целевой картине (TLM, вычисляемая проекция) — обоснованность и симметрия каркасу.

    Решение без живой ссылки на TB/PRB — «целевой элемент не обоснован». Каждый из 11 уровней
    каркаса — либо покрыт хотя бы одним решением (целевой образ спроектирован), либо несёт явную
    пометку «без изменений» в решении/примечании на этот уровень, либо (если на уровне была
    доказанная проблема AS-IS) — «белое пятно TO-BE».
    """
    memory = load_memory(Path(workspace))
    tb_index = _index_by(_rows(memory, "TB"), "код требования")
    prb_index = _index_by(_rows(memory, "PRB"), "код проблемы")
    sol_rows = _rows(memory, "SOL")

    дефекты: list = []
    по_уровню_sol: dict = defaultdict(list)

    for row in sol_rows:
        код = row.data.get("код решения")
        требование = row.data.get("закрываемое требование")
        проблема = row.data.get("устраняемая проблема")
        уровень_текст = row.data.get("уровень устранения")

        недостачи = []
        if not (требование and требование in tb_index):
            недостачи.append("«закрываемое требование» не резолвится в TB")
        if not (проблема and проблема in prb_index):
            недостачи.append("«устраняемая проблема» не резолвится в PRB")
        if недостачи:
            дефекты.append({"код решения": код, "дефект": "целевой элемент не обоснован",
                            "детали": недостачи})
            continue

        try:
            номер = int(str(уровень_текст).split(" ", 1)[0])
        except (TypeError, ValueError):
            дефекты.append({"код решения": код, "дефект": "«уровень устранения» не резолвится в номер",
                            "уровень устранения": уровень_текст})
            continue
        по_уровню_sol[номер].append(row)

    # доказанные (выше гипотезы) проблемы AS-IS по уровню — материал для белых пятен
    prb_доказано_по_уровню: dict = defaultdict(list)
    for row in _rows(memory, "PRB"):
        if row.data.get("статус доказанности") not in _ВЫШЕ_ГИПОТЕЗЫ:
            continue
        try:
            lvl = int(str(row.data.get("координата 1 — уровень (номер)")))
        except (TypeError, ValueError):
            continue
        prb_доказано_по_уровню[lvl].append(row.data.get("код проблемы"))

    по_уровням = []
    белые_пятна = []
    for lv in _framework_levels():
        номер, название = lv["номер"], lv["название"]
        решения_уровня = по_уровню_sol.get(номер, [])
        без_изменений = any(
            _БЕЗ_ИЗМЕНЕНИЙ in str(r.data.get("примечание") or "").lower()
            or _БЕЗ_ИЗМЕНЕНИЙ in str(r.data.get("обоснование выбора") or "").lower()
            for r in решения_уровня
        )
        целевой_образ_есть = bool(решения_уровня)
        доказанные_проблемы = prb_доказано_по_уровню.get(номер, [])

        if без_изменений:
            статус = "без изменений"          # явная пометка первична — даже при наличии строки SOL
        elif целевой_образ_есть:
            статус = "спроектирован"
        else:
            статус = "не спроектирован"

        по_уровням.append({
            "уровень": номер, "название": название, "статус": статус,
            "решений уровня": [r.data.get("код решения") for r in решения_уровня],
            "доказанные проблемы AS-IS уровня": доказанные_проблемы,
        })

        if статус == "не спроектирован" and доказанные_проблемы:
            белые_пятна.append({
                "уровень": номер, "название": название,
                "доказанные проблемы без целевого образа": доказанные_проблемы,
                "описание": f"Уровень {номер} «{название}» несёт доказанную проблему AS-IS, "
                            f"но не имеет ни целевого образа (SOL), ни пометки «без изменений».",
            })

    return {
        "этап": "осмысление — целевая картина по уровням (TLM, вычисляемая проекция из SOL)",
        "счётчики": {
            "SOL в памяти": len(sol_rows),
            "уровней каркаса": len(по_уровням),
            "уровней с целевым образом": sum(1 for л in по_уровням if л["статус"] == "спроектирован"),
            "уровней «без изменений»": sum(1 for л in по_уровням if л["статус"] == "без изменений"),
            "дефектов": len(дефекты),
            "белых пятен TO-BE": len(белые_пятна),
        },
        "дефекты": дефекты,
        "по_уровням": по_уровням,
        "белые_пятна": белые_пятна,
        "правило": "TLM не пишется руками (представление, C3) — вычисляется из SOL.уровень "
                   "устранения; каждый уровень каркаса либо спроектирован (SOL), либо помечен "
                   "«без изменений», либо (при доказанной AS-IS проблеме) — белое пятно TO-BE",
    }
