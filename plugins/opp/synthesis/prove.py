"""cap-t-prove — движок «доказать» T-конвейера осмысления (синтез AS-IS).

Отчёт по доказательствам ущерба (граница C13, спека 06-c-synthesis): проверяем НАЛИЧИЕ и
СВЯЗНОСТЬ доказательства — не правдоподобие величины. Это РЕЛЬСЫ, не суждение: гейт ОБЭ
(категория, «в чём ущерб», ссылка на EFF) уже держит `linter/checks.py` (`_check_gate_obe`,
живёт в `./opp lint`) — здесь тот же порог доказанности переиспользуется (`_ВЫШЕ_ГИПОТЕЗЫ`),
не дублируется. Прибавка prove поверх lint — арифметика: множители EFF (объём/ставка/частота)
резолвятся в VP/число, и «величина ущерба» проверяется как воспроизводимое произведение.

Переиспользует: `linter.model.load_memory` (строки проекта), `linter.checks._ВЫШЕ_ГИПОТЕЗЫ`
(порог доказанности, откуда действует гейт ОБЭ).
"""

from __future__ import annotations
from pathlib import Path

from linter.model import load_memory
from linter.checks import _ВЫШЕ_ГИПОТЕЗЫ

_ДОПУСК = 0.01  # 1% — расхождение записанной величины с пересчётом


def _index_by(rows: list, key: str) -> dict:
    return {r.data.get(key): r for r in rows if r.data.get(key) not in (None, "")}


def _rows(memory, table) -> list:
    return [r for r in memory.rows if r.table == table]


def _to_number(v):
    if isinstance(v, (int, float)) and not isinstance(v, bool):
        return v
    try:
        return float(str(v).replace(",", "."))
    except (TypeError, ValueError):
        return None


def _resolve_multiplier(value, vp_index: dict):
    """Множитель — ссылка на VP (берём «Значение») либо голое число. Не резолвится → None."""
    if value in (None, ""):
        return None
    num = _to_number(value)
    if num is not None:
        return num
    vp = vp_index.get(value)
    if vp is None:
        return None
    return _to_number(vp.data.get("Значение"))


def _eff_verdict(row, vp_index: dict) -> dict:
    """Проверка одной строки EFF: резолв множителей + воспроизводимость величины."""
    код = row.data.get("код эффекта")
    объём = row.data.get("объём (множитель)")
    ставка = row.data.get("ставка (множитель)")
    частота = row.data.get("частота (множитель)")
    величина = _to_number(row.data.get("величина ущерба"))

    объём_число = _resolve_multiplier(объём, vp_index)
    ставка_число = _resolve_multiplier(ставка, vp_index)
    частота_число = _resolve_multiplier(частота, vp_index) if частота not in (None, "") else 1

    множители_заданы = bool(объём) or bool(ставка) or (частота not in (None, ""))
    все_резолвятся = множители_заданы and объём_число is not None and ставка_число is not None and частота_число is not None

    verdict = {
        "код эффекта": код, "проблема": row.data.get("проблема"),
        "множители_заданы": множители_заданы, "все_множители_резолвятся": все_резолвятся,
        "пересчёт": None, "величина_записана": величина, "дефект": None,
    }
    if все_резолвятся:
        пересчёт = объём_число * ставка_число * частота_число
        verdict["пересчёт"] = пересчёт
        if величина is None:
            verdict["дефект"] = "ожидает данных"  # множители есть, величина ещё не сведена — не дефект
        elif пересчёт == 0:
            if величина != 0:
                verdict["дефект"] = "величина не воспроизводится"
        elif abs(величина - пересчёт) / abs(пересчёт) > _ДОПУСК:
            verdict["дефект"] = "величина не воспроизводится"
        return verdict

    if множители_заданы:
        # заданы, но не все резолвятся (битая ссылка/не число) — не судим правдоподобие,
        # просто не можем подтвердить связность; отдельного дефекта не заводим (это ловит lint).
        verdict["дефект"] = None
        return verdict

    # множителей нет вовсе
    if величина is not None:
        verdict["дефект"] = "величина без множителей"       # оценка на глаз (запрещена методикой р.6)
    else:
        verdict["дефект"] = "ожидает данных"                # честно пусто — не дефект
    return verdict


def prove_report(workspace) -> dict:
    """Отчёт по доказательствам ущерба: полнота гейта ОБЭ + воспроизводимость величины EFF.

    По каждой PRB выше «гипотезы» — полнота гейта (категория, «в чём ущерб», живая ссылка на
    EFF); по каждой EFF — резолв множителей и пересчёт «объём × ставка × частота» против
    записанной «величины ущерба». Кандидаты без доказательства — отдельным списком, не смешаны
    со счётом дефектов (это не проблема, а материал для дальнейшей работы по методике р.6).
    """
    memory = load_memory(Path(workspace))
    eff_index = _index_by(_rows(memory, "EFF"), "код эффекта")
    vp_index = _index_by(_rows(memory, "VP"), "Код параметра")

    дефекты: list = []
    кандидаты: list = []
    prb_проверено = 0

    for row in _rows(memory, "PRB"):
        код = row.data.get("код проблемы")
        статус = row.data.get("статус доказанности")
        if str(row.data.get("нижняя отсечка", "")).startswith("ниже порога"):
            continue  # особенность учёта — гейт ОБЭ не применяется (методика р.6)

        if статус not in _ВЫШЕ_ГИПОТЕЗЫ:
            категория = row.data.get("координата 3 — ОБЭ: категория")
            в_чём = row.data.get("координата 3 — ОБЭ: в чём ущерб")
            if not категория or not в_чём:
                кандидаты.append({
                    "код проблемы": код, "статус доказанности": статус,
                    "причина": "нет категории ОБЭ" if not категория else "нет «в чём ущерб»",
                })
            continue

        prb_проверено += 1
        категория = row.data.get("координата 3 — ОБЭ: категория")
        в_чём = row.data.get("координата 3 — ОБЭ: в чём ущерб")
        расчёт = row.data.get("координата 3 — ОБЭ: сколько (ссылка на расчёт)")
        eff_row = eff_index.get(расчёт) if расчёт else None

        if not (категория and в_чём and eff_row is not None):
            дефекты.append({
                "код проблемы": код, "статус доказанности": статус,
                "дефект": "гейт ОБЭ неполон",
                "детали": {"категория": bool(категория), "в чём ущерб": bool(в_чём),
                          "EFF-ссылка живая": eff_row is not None},
            })
            continue

        verdict = _eff_verdict(eff_row, vp_index)
        if verdict["дефект"] and verdict["дефект"] != "ожидает данных":
            дефекты.append({
                "код проблемы": код, "статус доказанности": статус,
                "дефект": verdict["дефект"], "код эффекта": verdict["код эффекта"],
                "величина_записана": verdict["величина_записана"], "пересчёт": verdict["пересчёт"],
            })

    return {
        "этап": "1.8 — доказательство ущерба (гейт ОБЭ + воспроизводимость EFF)",
        "счётчики": {
            "PRB выше гипотезы (проверено гейтом)": prb_проверено,
            "EFF в памяти": len(eff_index),
            "дефектов": len(дефекты),
            "кандидатов без доказательства": len(кандидаты),
        },
        "дефекты": дефекты,
        "кандидаты_не_проблемы": кандидаты,
        "правило": "проверяем наличие и связность доказательства (категория, «в чём», EFF, "
                   "арифметика множителей) — не правдоподобие величины (граница C13)",
    }
