"""cap-t-requirements — движок «требования» T-конвейера осмысления (синтез AS-IS).

Отчёт по требованиям и покрытию проблем (спека 06-c-synthesis): TB несёт устраняемый ОБЭ (EFF),
критерий приёмки и бизнес-результат; трассировка «требование → проблема» держится двумя полями
самой строки TB — «покрывает проблемы (полностью)» и «...(частично)» (TBPR поглощена этими
полями, ЗР-0024, п.1). Замкнутость PRB×TB как таковая уже проверяет `linter/checks.py`
(`_check_closure_prb_tb`, живёт в `./opp lint`) — здесь не дублируем эту проверку, а даём
рабочий отчёт: какие проблемы не покрыты ни одним требованием и какие требования висят без
проблемы, плюс полнота обязательных полей TB (методика р.7). Висячие ссылки внутри полей
покрытия (код PRB, который не резолвится) ловит общая проверка замкнутости памяти
(`linter.checks.check_memory` — рёбра TB→PRB резолвятся по контракту как любые другие) —
здесь не дублируем и её.

Переиспользует: `linter.model.load_memory` (строки проекта), `linter.checks._ВЫШЕ_ГИПОТЕЗЫ`
(порог доказанности, с которого проблема ожидает требование-ответ), `linter.checks._as_list`
(нормализация значения ссылочного поля в список).
"""

from __future__ import annotations
from pathlib import Path

from linter.model import load_memory
from linter.checks import _ВЫШЕ_ГИПОТЕЗЫ, _as_list

# ЗР-0024 п.1: экс-TBPR — два поля покрытия на строке TB вместо отдельной связки.
_ПОЛЯ_ПОКРЫТИЯ = ("покрывает проблемы (полностью)", "покрывает проблемы (частично)")


def _rows(memory, table) -> list:
    return [r for r in memory.rows if r.table == table]


def _index_by(rows: list, key: str) -> dict:
    return {r.data.get(key): r for r in rows if r.data.get(key) not in (None, "")}


def requirements_report(workspace) -> dict:
    """Отчёт по требованиям (TB) и покрытию проблем.

    TB: «устраняемый ОБЭ» резолвится в существующую EFF, «критерий приёмки» и «бизнес-результат»
    непусты — иначе дефект. Покрытие проблем — объединение полей «покрывает проблемы
    (полностью)»/«...(частично)» по всем строкам TB. «Непокрытые проблемы» — PRB выше
    «гипотезы» (и не ниже нижней отсечки), код которой не встречается ни в одном из полей
    покрытия ни одного TB. «Требования без проблемы» — TB, у которого оба поля покрытия пусты
    (методика р.7: пожелание, а не требование).
    """
    memory = load_memory(Path(workspace))
    eff_index = _index_by(_rows(memory, "EFF"), "код эффекта")
    tb_index = _index_by(_rows(memory, "TB"), "код требования")

    дефекты: list = []

    # TB: полнота обязательных полей
    for row in _rows(memory, "TB"):
        код = row.data.get("код требования")
        устраняемый_обэ = row.data.get("устраняемый ОБЭ")
        критерий = row.data.get("критерий приёмки")
        результат = row.data.get("бизнес-результат")
        недостачи = []
        if not (устраняемый_обэ and устраняемый_обэ in eff_index):
            недостачи.append("устраняемый ОБЭ не резолвится в EFF")
        if not критерий:
            недостачи.append("критерий приёмки пуст")
        if not результат:
            недостачи.append("бизнес-результат пуст")
        if недостачи:
            дефекты.append({"код требования": код, "дефект": "TB неполно", "детали": недостачи})

    # покрытие проблем: объединение обоих полей TB по всем строкам + счётчик пар покрытия
    покрытые_проблемы = set()
    связки_требований = set()
    пар_покрытия = 0
    for row in _rows(memory, "TB"):
        код = row.data.get("код требования")
        покрытие_строки = set()
        for поле in _ПОЛЯ_ПОКРЫТИЯ:
            значения = _as_list(row.data.get(поле))
            покрытие_строки |= set(значения)
            пар_покрытия += len(значения)
        if покрытие_строки:
            связки_требований.add(код)
        покрытые_проблемы |= покрытие_строки

    непокрытые_проблемы = []
    for row in _rows(memory, "PRB"):
        код = row.data.get("код проблемы")
        статус = row.data.get("статус доказанности")
        if str(row.data.get("нижняя отсечка", "")).startswith("ниже порога"):
            continue
        if статус in _ВЫШЕ_ГИПОТЕЗЫ and код not in покрытые_проблемы:
            непокрытые_проблемы.append({"код проблемы": код, "статус доказанности": статус})

    требования_без_проблемы = [
        {"код требования": row.data.get("код требования"),
         "статус требования": row.data.get("статус требования")}
        for row in _rows(memory, "TB")
        if row.data.get("код требования") not in связки_требований
    ]

    return {
        "этап": "синтез: требования (TB) и покрытие проблем",
        "счётчики": {
            "TB в памяти": len(tb_index),
            "пар покрытия": пар_покрытия,
            "дефектов": len(дефекты),
            "непокрытых проблем": len(непокрытые_проблемы),
            "требований без проблемы": len(требования_без_проблемы),
        },
        "дефекты": дефекты,
        "непокрытые_проблемы": непокрытые_проблемы,
        "требования_без_проблемы": требования_без_проблемы,
        "правило": "требование = бизнес-результат с критерием приёмки и устраняемым ОБЭ; "
                   "каждая проблема выше гипотезы покрыта хотя бы одним TB (полностью или "
                   "частично), требований «из воздуха» не бывает (методика р.7)",
    }
