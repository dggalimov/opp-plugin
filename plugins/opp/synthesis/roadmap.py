"""cap-t-roadmap — движок «дорожная карта» T-конвейера осмысления (Ф5: осмысление, спека 06-d).

Отчёт по дорожной карте (RM, контракт: строка несёт одну из двух ролей — «волна (очередь)» или
«работа/блок», работа ссылается на волну полем «Волна (очередь)»): каждая строка обязана нести
непустые живые ссылки на решения/TO-BE («Реализуемые требования»→TB обязательно, «Реализуемые
решения»→SOL — если задано) — иначе «работа без основания» (методика р.7, тот же принцип, что
RM.целостность). Порядок волн не должен нарушать зависимостей: граф «Зависит от (очерёдность)»
проверяется на ацикличность (нельзя доказать порядок при цикле). Денег и КРП в проверке НЕТ —
поля стоимости/сметы (`Оценка трудоёмкости`, `Ссылка на смету (КРП)`) не проверяются и не
требуются (спека 06-d, граница ЗР-0020).

Это РЕЛЬСЫ, не суждение: приоритезацию и разбивку на волны делает навык `skill-roadmap` — здесь
только проверка полноты наполнения и непротиворечивости уже написанных строк.

Переиспользует: `linter.model.load_memory` (строки проекта).
"""

from __future__ import annotations
from pathlib import Path

from linter.model import load_memory


def _rows(memory, table) -> list:
    return [r for r in memory.rows if r.table == table]


def _index_by(rows: list, key: str) -> dict:
    return {r.data.get(key): r for r in rows if r.data.get(key) not in (None, "")}


def _as_list(v) -> list:
    if v in (None, ""):
        return []
    return v if isinstance(v, list) else [v]


def _find_cycle(graph: dict) -> list:
    """Первый найденный цикл рефлексивного графа «код → [зависимости]», иначе пусто."""
    чист: set = set()
    for start in graph:
        if start in чист:
            continue
        путь: list = []
        на_пути: set = set()
        узел = start
        while узел is not None:
            if узел in на_пути:
                i = путь.index(узел)
                return путь[i:] + [узел]
            if узел in чист or узел not in graph:
                break
            путь.append(узел)
            на_пути.add(узел)
            следующие = [v for v in graph.get(узел, []) if v]
            узел = следующие[0] if следующие else None
        чист |= на_пути
    return []


def roadmap_report(workspace) -> dict:
    """Отчёт по дорожной карте (RM): наполнение волн и ацикличность очерёдности.

    Каждая строка RM (волна или работа) должна тянуть непустую живую ссылку «Реализуемые
    требования» (→TB); «Реализуемые решения» (→SOL), если заданы, тоже должны быть живыми.
    Волна («Уровень планирования» = «волна (очередь)») без единой работы, ссылающейся на неё
    через «Волна (очередь)», — пустая волна (дефект). Граф «Зависит от (очерёдность)» проверяется
    на ацикличность — цикл делает порядок волн недоказуемым. Денег/сметы (КРП) не проверяем —
    вне границы Ф5 (ЗР-0020).
    """
    memory = load_memory(Path(workspace))
    tb_index = _index_by(_rows(memory, "TB"), "код требования")
    sol_index = _index_by(_rows(memory, "SOL"), "код решения")
    rm_rows = _rows(memory, "RM")
    rm_index = _index_by(rm_rows, "Код элемента карты")

    дефекты: list = []
    работа_по_волне: dict = {}

    for row in rm_rows:
        код = row.data.get("Код элемента карты")
        уровень_план = row.data.get("Уровень планирования")
        требования = _as_list(row.data.get("Реализуемые требования"))
        решения = _as_list(row.data.get("Реализуемые решения"))
        волна = row.data.get("Волна (очередь)")
        зависит_от = _as_list(row.data.get("Зависит от (очерёдность)"))

        недостачи = []
        if уровень_план == "работа/блок":
            if not требования:
                недостачи.append("«реализуемые требования» пусты — работа без основания")
            else:
                висячие = [t for t in требования if t not in tb_index]
                if висячие:
                    недостачи.append(f"висячие ссылки в «реализуемые требования»: {висячие}")
            висячие_sol = [s for s in решения if s not in sol_index]
            if висячие_sol:
                недостачи.append(f"висячие ссылки в «реализуемые решения»: {висячие_sol}")
            if волна:
                if волна not in rm_index:
                    недостачи.append(f"«волна (очередь)» не резолвится в RM: {волна}")
                else:
                    работа_по_волне.setdefault(волна, []).append(код)

        висячие_завис = [d for d in зависит_от if d not in rm_index]
        if висячие_завис:
            недостачи.append(f"висячие ссылки в «зависит от (очерёдность)»: {висячие_завис}")

        if недостачи:
            дефекты.append({"код элемента карты": код, "дефект": "наполнение RM неполно",
                            "детали": недостачи})

    пустые_волны = []
    for row in rm_rows:
        if row.data.get("Уровень планирования") != "волна (очередь)":
            continue
        код = row.data.get("Код элемента карты")
        if not работа_по_волне.get(код):
            пустые_волны.append({"код элемента карты": код, "название": row.data.get("Название")})

    граф_очерёдности = {row.data.get("Код элемента карты"):
                         _as_list(row.data.get("Зависит от (очерёдность)")) for row in rm_rows}
    цикл = _find_cycle(граф_очерёдности)

    return {
        "этап": "осмысление — дорожная карта (RM): наполнение и очерёдность (без денег/КРП)",
        "счётчики": {
            "RM в памяти": len(rm_rows),
            "волн": sum(1 for r in rm_rows if r.data.get("Уровень планирования") == "волна (очередь)"),
            "работ/блоков": sum(1 for r in rm_rows if r.data.get("Уровень планирования") == "работа/блок"),
            "дефектов": len(дефекты),
            "пустых волн": len(пустые_волны),
            "цикл очерёдности": bool(цикл),
        },
        "дефекты": дефекты,
        "пустые_волны": пустые_волны,
        "цикл_очерёдности": цикл,
        "правило": "каждая строка RM тянет непустые живые ссылки на решения/TO-BE (р.7); волна "
                   "без работ — пустая; очерёдность волн ациклична; денег/КРП не проверяем (ЗР-0020)",
    }
