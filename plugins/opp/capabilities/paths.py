"""Владелец пути «Источники» — единственная точка правды (спека 08 §8, ЗР-0027 п.7).

Раньше путь к техслою узлов-источников был размазан по коду: `ingest.py:SOURCES_DIR`,
`schema/extract.py:_SOURCES_DIR` — две независимые константы — плюс литералы в deploy.py,
meetings.py, profiles.py. Клиентский слой отделяется от технического: «Источники» переезжают
ВНУТРЬ «Проектной памяти» (наверху проекта остаются только клиентские папки — Входные
материалы, Встречи, Документы, Проектная память, паспорт и правила).

`sources_dir()` также несёт разовую авто-миграцию старых проектов (структура ЗР-0025/06-f,
где «Источники» лежали в корне) — без нее перенос вручную даёт коллизию кодов SRC (нумерация
с 001 заново) и дубли (индекс хешей не находит старое).

`DOCS_TECH_REL`/`docs_tech_dir()` — техслой документов (спека 08 §8): md-канон рендера уходит
в «Проектная память/Документы» (клиентский `<ws>/Документы` остаётся только для docx с
человеческими именами, см. `capabilities/render/engine.resolve_output_name`). В отличие от
«Источники» — авто-миграции здесь нет (в старом проекте под «Документы» лежали md старого
формата рядом с docx; следующий рендер просто создаст новый техслойный путь, ничего не
переносит и не трогает).
"""

from __future__ import annotations
import sys
from pathlib import Path

PAMYAT = "Проектная память"
SOURCES_NAME = "Источники"
SOURCES_REL = f"{PAMYAT}/{SOURCES_NAME}"
DOCS_NAME = "Документы"
DOCS_TECH_REL = f"{PAMYAT}/{DOCS_NAME}"


def sources_dir(workspace) -> Path:
    """<workspace>/Проектная память/Источники — единственная точка правды пути источников.

    Идемпотентно и безопасно:
    - новый путь уже существует → просто возвращаем его (если ЗАОДНО существует и старый
      корневой «Источники» — это две копии, ничего не сливаем молча, только предупреждаем);
    - нового пути нет, а старый корневой `<ws>/Источники` существует и непуст → МИГРАЦИЯ:
      `Path.rename` на новое место (git подхватит перемещение как `git mv`, если репозиторий
      есть); узлы и коды кодификации остаются как есть — повторный ingest продолжает
      нумерацию, не плодит дублей;
    - ни новый, ни старый путь не существуют (свежий проект) → возвращаем путь, ничего не
      трогаем — создание директории остаётся на вызывающей стороне (deploy.py и т.п.).
    """
    workspace = Path(workspace)
    new = workspace / PAMYAT / SOURCES_NAME
    old = workspace / SOURCES_NAME

    if new.exists():
        if old.exists():
            print(f'OPP: внимание — существуют обе папки источников («{old}» и «{new}»); '
                  f'использую новую, старую разберите вручную (ЗР-0027)', file=sys.stderr)
        return new

    if old.is_dir() and any(old.iterdir()):
        new.parent.mkdir(parents=True, exist_ok=True)
        old.rename(new)
        print('OPP: структура обновлена — «Источники» перенесены в «Проектную память» (ЗР-0027)',
              file=sys.stderr)

    return new


def docs_tech_dir(workspace) -> Path:
    """<workspace>/Проектная память/Документы — техслойный путь md-канона документов (спека 08 §8).

    Только путь, без миграции и без mkdir (создание — на вызывающей стороне, как и раньше у
    клиентского «Документы»): у техслоя нет старого места для переноса — до ЗР-0027 md-канон
    лежал рядом с docx в клиентском `<ws>/Документы`, следующий рендер просто создаст новый путь.
    """
    return Path(workspace) / PAMYAT / DOCS_NAME
